    public void insertToObj(Grade gr) {
        id = gr.getId();
        jlbhoTen.setText(gr.getStudents().getStudentName());
        jlbDtb.setText(gr.getMedium_Score() + "");
        txtmaSv.setText(gr.getStudents().getStudentID());
        txtmaSv.setEnabled(false);
        txtTa.setText(gr.getEnglish_Score() + "");
        txtTinhoc.setText(gr.getIT_Score() + "");
        txtGdtc.setText(gr.getPE_Score() + "");
        btnDel.setEnabled(true);
        btnSave.setEnabled(true);
    }

    public void reset() {
        jlbhoTen.setText("");
        jlbDtb.setText("");
        txtmaSv.setText("");
        txtmaSv.setEnabled(true);
        txtTa.setText("");
        txtTinhoc.setText("");
        txtGdtc.setText("");
        check = -1;
        id = -1;
        btnDel.setEnabled(false);
        btnSave.setEnabled(false);
        txtTim.setText("");
    }

    public void showTb() {
        model.setRowCount(0);
        List<Grade> list = service.selectAll();
        if (list.isEmpty()) {
            btnFirt.setEnabled(false);
            btnNext.setEnabled(false);
            btnPrec.setEnabled(false);
            btnEnd.setEnabled(false);
        } else {
            btnFirt.setEnabled(true);
            btnNext.setEnabled(true);
            btnPrec.setEnabled(true);
            btnEnd.setEnabled(true);
            list.forEach((t) -> {
                model.addRow(t.getObj());
            });

        }

    }

