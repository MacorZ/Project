    private void showTb() {
        model.setRowCount(0);
        List<Ban> list = banservice.getAll();
        list.forEach((t) -> {
            model.addRow(t.getobj());
        });
    }

    public Ban insertForm() {
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        String soThich = txtSoThich.getText();
        Date ngaySinh = dateNgaySinh.getDate();
        int cll = chackConll.isSelected() ? 0 : 1;
        return new Ban(id,ma, ten, ngaySinh, soThich, cll);
    }

    public void insertTB(int index) {
        Ban b = new Ban();
        b.setMaBan(model.getValueAt(index, 0).toString());
        id = banservice.getOne(b).getId();
        txtMa.setText(model.getValueAt(index, 0).toString());
        txtMa.setEnabled(false);
        txtTen.setText(model.getValueAt(index, 1).toString());
        txtSoThich.setText(model.getValueAt(index, 3).toString());
        boolean cll = model.getValueAt(index, 4).toString().equalsIgnoreCase("Còn lien lạc") ? true : false;
        chackConll.setSelected(cll);
        String dates = model.getValueAt(index, 2).toString();

        Date date;
        try {
            date = new SimpleDateFormat("dd/MM/yyyy").parse(dates);
            dateNgaySinh.setDate(date);
        } catch (ParseException ex) {
            ex.printStackTrace();
        }

    }

    private void reset() {
        check = -1;
        id = "";
        txtMa.setText("");
        txtMa.setEnabled(true);
        txtTen.setText("");
        txtSoThich.setText("");
        chackConll.setSelected(false);
        Date date = new Date();
        dateNgaySinh.cleanup();

    }
