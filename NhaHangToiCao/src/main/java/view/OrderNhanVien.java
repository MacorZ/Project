    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String ten = jTextField_tenKh.getText();
        String tenMon = jComboBox3.getSelectedItem().toString();
        int donGia = Integer.parseInt(jLabel_donGia.getText().split(" ")[0]);
        int thanhTien = Integer.parseInt(jLabel_thanhTien.getText().split(" ")[0]);
        int sl = Integer.parseInt(jSpinner1.getValue().toString());
        DonHang donHang = new DonHang(ten, tenMon, donGia,sl, thanhTien);
        String add = DonHangService.callClass().add(donHang);
        if(!add.equals("")){
                    JOptionPane.showMessageDialog(rootPane,add);
                    return;

        }
        JOptionPane.showMessageDialog(rootPane,"Thanh cong");
        showtb();

    }//GEN-LAST:event_jButton2ActionPerformed

