    public void showtb(){
        model.setRowCount(0);
        List<DonHang> list = DonHangService.callClass().getAll();
        list.forEach((t) -> {
          model.addRow(new Object[]{
              t.getTenKhachHang(),t.getTenMonAn(),t.getDonGia(),t.getSoDonHang(),t.getThanhTien(),t.getId()
          });
        });
    }
