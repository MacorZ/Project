    public Grade insertToForm() {
        String ma = txtmaSv.getText();
        Students students = new Students();
        students.setStudentID(ma);
        String check = service.checkSo(txtTa.getText(), txtTinhoc.getText(), txtGdtc.getText());
        if (!check.equals("")) {
            JOptionPane.showMessageDialog(rootPane, check);
            return null;
        }
        int ta = Integer.parseInt(txtTa.getText());
        int th = Integer.parseInt(txtTinhoc.getText());
        int gdtc = Integer.parseInt(txtGdtc.getText());
        return new Grade(id, students, ta, th, gdtc);
    }

