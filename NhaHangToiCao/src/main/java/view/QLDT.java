    public void showtb() {
        model.setRowCount(0);
        List<TaiKhoan> list = TaiKhoanService.callClass().getAll();
        list.forEach((t) -> {
            model.addRow(new Object[]{
                t.getTenTaiKhoan(), t.getMatKhau(), t.getTenNV()

            });
        });
    }

    public void reset() {
        jTextField_tk.setText("");
        jTextField_ten.setText("");
        jTextField_mk.setText("");
        check = -1;
        jTextField_tk.setEnabled(true);
    }

