    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (check == -1) {
            JOptionPane.showMessageDialog(rootPane, "Chua chon dong can sua");
            return;
        }
        String ttk = jTextField_tk.getText();
        String mk = jTextField_mk.getText();
        String ten = jTextField_ten.getText();
        TaiKhoan tk = new TaiKhoan(ttk, mk, ten);
        String check = TaiKhoanService.callClass().check(tk);
        if (!check.equals("")) {
            JOptionPane.showMessageDialog(rootPane, check);
            return;
        }
        check = TaiKhoanService.callClass().update(tk);
        if (!check.equals("")) {
            JOptionPane.showMessageDialog(rootPane, check);
            return;
        }
        showtb();
        reset();
        JOptionPane.showMessageDialog(rootPane, "Cap nhat thanh cong");
    }//GEN-LAST:event_jButton3ActionPerformed

