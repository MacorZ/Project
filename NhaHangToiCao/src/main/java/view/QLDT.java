    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String ttk = jTextField_tk.getText();
        String mk = jTextField_mk.getText();
        String ten = jTextField_ten.getText();
        TaiKhoan tk = new TaiKhoan(ttk, mk, ten);
        String check = TaiKhoanService.callClass().check(tk);
        if (!check.equals("")) {
            JOptionPane.showMessageDialog(rootPane, check);
            return;
        }
        check = TaiKhoanService.callClass().add(tk);
        if (!check.equals("")) {
            JOptionPane.showMessageDialog(rootPane, check);
            return;
        }
        showtb();
        reset();
        JOptionPane.showMessageDialog(rootPane, "Them thanh cong");

    }//GEN-LAST:event_jButton2ActionPerformed

